async def _build_context(
    org_id: UUID,
    case_id: UUID,
    source: str,
    table: str,
    ref: str,
    field: str,
) -> dict:
    """Gather case + finding context for the LLM prompt."""
    async with get_conn() as conn:
        case_row = await conn.fetchrow(
            """
            SELECT short_id, status, case_type, customer_ref, amount_minor,
                   currency, decision_action, decision_amount_minor,
                   decision_confidence, thread_id
            FROM cases
            WHERE org_id=$1 AND id=$2
            """,
            org_id, case_id,
        )
        if case_row is None:
            return {"finding_texts": [], "tldr": None}

        # The brief TLDR (if any) - drops the postmortem summary in.
        brief_row = await conn.fetchrow(
            """
            SELECT data FROM events
            WHERE org_id=$1 AND thread_id=$2 AND type='brief_drafted'
            ORDER BY seq DESC LIMIT 1
            """,
            org_id, case_row["thread_id"],
        )
        tldr = None
        if brief_row:
            data = brief_row["data"]
            if isinstance(data, str):
                data = json.loads(data)
            tldr = (data or {}).get("tldr")

        # Findings that cite this exact (source, table, ref). We
        # check each finding's citations array for a match.
        finding_rows = await conn.fetch(
            """
            SELECT seq, text, citations
            FROM findings
            WHERE org_id=$1 AND case_id=$2
            ORDER BY seq ASC
            """,
            org_id, case_id,
        )

    finding_texts: list[str] = []
    for f in finding_rows:
        cites = f["citations"]
        if isinstance(cites, str):
            try:
                cites = json.loads(cites)
            except Exception:
                cites = []
        if not isinstance(cites, list):
            continue
        for c in cites:
            if not isinstance(c, dict):
                continue
            if (
                c.get("source") == source
                and c.get("table") == table
                and c.get("ref") == ref
            ):
                finding_texts.append(f["text"])
                break

    return {
        "short_id": case_row["short_id"],
        "case_type": case_row["case_type"],
        "customer_ref": case_row["customer_ref"],
        "amount_minor": case_row["amount_minor"],
        "currency": case_row["currency"],
        "decision_action": case_row["decision_action"],
        "decision_amount_minor": case_row["decision_amount_minor"],
        "decision_confidence": (
            float(case_row["decision_confidence"])
            if case_row["decision_confidence"] is not None
            else None
        ),
        "tldr": tldr,
        "finding_texts": finding_texts,
    }


def _build_prompt(
    ctx: dict,
    source: str,
    table: str,
    ref: str,
    field: str,
) -> str:
    amt = ctx.get("amount_minor")
    amt_str = (
        f"${(amt or 0) / 100:,.0f} {(ctx.get('currency') or 'usd').upper()}"
        if amt else "-"
    )
    decision = ctx.get("decision_action") or "(pending)"
    conf = ctx.get("decision_confidence")
    conf_str = f"{int(conf * 100)}%" if conf is not None else "-"
    findings = ctx.get("finding_texts") or []
    finding_block = (
        "\n".join(f"  - {t}" for t in findings[:6])
        if findings
        else "  (no finding explicitly cites this entry)"
    )

    return (
        f"CASE: {ctx.get('customer_ref') or 'Unknown customer'} · "
        f"{ctx.get('case_type') or 'case'} · {amt_str}\n"
        f"DECISION: {decision} (confidence {conf_str})\n"
        f"BRIEF TLDR: {ctx.get('tldr') or '(none yet)'}\n\n"
        f"CITATION:\n"
        f"  Source: {source}\n"
        f"  Entry: {table} / {ref}\n"
        f"  Specific aspect: {field or '(the entry as a whole)'}\n\n"
        f"FINDINGS that cite this entry:\n"
        f"{finding_block}\n\n"
        f"Explain in 2-3 plain-English sentences why this entry matters for "
        f"the decision above."
    )


def _normalize(s: str) -> str:
    """Strip wrapping quotes, collapse whitespace, drop boilerplate prefix."""
    s = s.strip().strip('"').strip("'").strip()
    s = " ".join(s.split())
    for prefix in ("Reasoning:", "Explanation:", "Answer:", "Citation:"):
        if s.lower().startswith(prefix.lower()):
            s = s[len(prefix):].strip()
    return s[:1200]


def _fallback(
    source: str,
    table: str,
    ref: str,
    field: str | None,
    ctx: dict,
    *,
    model: str | None,
) -> CitationReasoningResult:
    """Deterministic fallback when the LLM call fails / no API key."""
    findings = ctx.get("finding_texts") or []
    if findings:
        snippet = findings[0]
        # Keep it short
        if len(snippet) > 240:
            snippet = snippet[:237] + "…"
        reasoning = (
            f"This {source} entry ({table} / {ref}) is cited in the "
            f"investigation: {snippet}"
        )
    else:
        reasoning = (
            f"This {source} entry was referenced by the agent while "
            f"investigating the case. Open the source to see the full record."
        )
    return CitationReasoningResult(
        source=source,
        table=table,
        ref=ref,
        field=field,
        reasoning=reasoning,
        generated_at=datetime.utcnow(),
        model=model,
        cached=False,
    )
